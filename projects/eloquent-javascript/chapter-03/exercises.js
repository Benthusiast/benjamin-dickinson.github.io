////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  
    if(x > y){
      return y;
    }else{
      return x;
    }
  
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
  if(x % 2 === 0){
    return true;
  }else{
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var array = string.split("");
  var counter = 0;
  for(var i = 0; i < array.length; i++){
    if(array[i] === char){
      counter++
    }
  }
  return counter;  
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var array = string.split("");
  var counter = 0;
  for(var i = 0; i < array.length; i++){
    if(array[i] === "B"){
      counter++
    }
  }
  return counter;
}

